    private void doClose(int retStatus) {
        try {
            if (retStatus == RET_OK) {
                if(ManterUsuario.login(login.getText(), senha.getText())) {
                    
                    MenuGerencia m = new MenuGerencia();
                    m.setVisible(true);
                    m.setLocationRelativeTo(null);
                }
                else {
                    JOptionPane.showMessageDialog(rootPane, "Login ou senha incorretos.", "Erro", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            returnStatus = retStatus;
            setVisible(false);
            dispose();
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro inesperado.", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Falha na conexão com o banco de dados.", JOptionPane.ERROR_MESSAGE);
        }
    }
    
